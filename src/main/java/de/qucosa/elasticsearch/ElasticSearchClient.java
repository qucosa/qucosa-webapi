package de.qucosa.elasticsearch;

import org.elasticsearch.client.transport.TransportClient;
import org.elasticsearch.common.settings.Settings;
import org.elasticsearch.common.transport.InetSocketTransportAddress;
import org.springframework.beans.factory.DisposableBean;

public class ElasticSearchClient
    extends TransportClient implements DisposableBean {

    public ElasticSearchClient(Settings settings, String hostname, int port) {
        super(settings);
        addTransportAddress(new InetSocketTransportAddress(hostname, port));
    }

    @Override
    public void destroy() throws Exception {
        close();
    }
}
